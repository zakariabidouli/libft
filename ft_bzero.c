#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*x;

	x = (char *)s;
	i = 0;
	while (i < n)
	{
		x[i] = '\0';
		i++;
	}
	return (s);
}
